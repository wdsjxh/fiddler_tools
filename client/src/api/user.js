// flask server
export function login(data) {
  return request({
    url: '/login',
    method: 'post',
    data
  })
}

export function register(data) {
  return request({
    url: '/register',
    method: 'post',
    data
  })
}

export function getInfo(token) {
  return request({
    url: '/user/info',
    method: 'get',
    params: { token }
  })
}

export function dashBoard(params) {
  return request({
    url: '/dashboard',
    method: 'get',
    params
  })
}

export function logout() {
  return request({
    url: '/user/logout',
    method: 'post'
  })
}

export function make_capture_token(data) {
  return request({
    url: '/user/make_capture_token',
    method: 'post',
    data
  })
}
